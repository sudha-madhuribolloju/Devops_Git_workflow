#Formatted Strings (F-strings) in Python
name = "Sudha"
age = 30
city = "Hyderabad"
# Using f-strings (formatted string literals) #more readable and concise
formatted_string = f"{name} is {age} years old and lives in {city}."
print(formatted_string)
